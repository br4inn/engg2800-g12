// WRITE COMMAND  xxxxxxxxxxxxxxxxxxx
void EPD_W21_WriteCMD(unsigned char INIT_COM)
{
	
	//SPCR = ((1 << SPE) | (1 << MSTR));
	
	PORTB &= ~(1 << CS);
	PORTB &= ~(1 << DC);
	
	SPDR = INIT_COM;
	
	while(!(SPSR & (1 << SPIF)));
	
	PORTB |= (1 << CS);
	PORTB |= (1 << DC);
		
}

// WRITE DATA  xxxxxxxxxxxxxxxxxxxxx
void EPD_W21_WriteDATA(unsigned char INIT_DATA)
{

	//SPCR = (1 << SPE) | (1 << MSTR) | (1 << SPR0);
	
	PORTB &= ~(1 << CS);
	
	SPDR = INIT_DATA;
	
	while(!(SPSR & (1 << SPIF)));
	
	PORTB |= (1 << CS);
	
}




